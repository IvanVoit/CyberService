/* ── Apply language ── */
function applyLanguage(lang) {
    const t = translations[lang];
    if (!t) return;

    // Text content
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key] !== undefined) el.innerHTML = t[key];
    });

    // Placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (t[key] !== undefined) el.placeholder = t[key];
    });

    // Update lang label
    const labels = { en: 'EN', es: 'ES', ca: 'CA', de: 'DE', ru: 'RU', uk: 'UA' };
    document.getElementById('currentLang').textContent = labels[lang] || lang.toUpperCase();

    // Mark active in dropdown
    document.querySelectorAll('.lang-dropdown li').forEach(li => {
        li.classList.toggle('active', li.dataset.lang === lang);
    });

    document.documentElement.lang = lang;
    localStorage.setItem('cs_lang', lang);
}

/* ── Language dropdown toggle ── */
document.addEventListener('DOMContentLoaded', () => {
    const langBtn      = document.getElementById('langBtn');
    const langDropdown = document.getElementById('langDropdown');

    langBtn.addEventListener('click', (e) => {
        e.stopPropagation();
        const isOpen = langDropdown.classList.toggle('open');
        langBtn.classList.toggle('open', isOpen);
        langBtn.setAttribute('aria-expanded', isOpen);
    });

    langDropdown.querySelectorAll('li').forEach(li => {
        li.addEventListener('click', () => {
            applyLanguage(li.dataset.lang);
            langDropdown.classList.remove('open');
            langBtn.classList.remove('open');
            langBtn.setAttribute('aria-expanded', false);
        });
    });

    document.addEventListener('click', () => {
        langDropdown.classList.remove('open');
        langBtn.classList.remove('open');
        langBtn.setAttribute('aria-expanded', false);
    });

    // Load saved language (default: English)
    const saved = localStorage.getItem('cs_lang') || 'en';
    applyLanguage(saved);

    /* ── Smooth scroll for ALL nav links + CTA button ── */
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            const target = document.querySelector(this.getAttribute('href'));
            if (!target) return;
            e.preventDefault();
            window.scrollTo({
                top: target.offsetTop - 70,
                behavior: 'smooth'
            });
        });
    });

    /* ── Form submission ── */
    const orderForm = document.getElementById('orderForm');
    if (orderForm) {
        orderForm.addEventListener('submit', () => {
            alert("Thank you! You are now being redirected to complete the verification.");
        });
    }

    /* ── Scroll-reveal: service cards ── */
    const observerOptions = { threshold: 0.1 };
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.style.opacity = '1';
                entry.target.style.transform = 'translateY(0)';
            }
        });
    }, observerOptions);

    document.querySelectorAll('.service-card').forEach(card => {
        card.style.opacity = '0';
        card.style.transform = 'translateY(20px)';
        card.style.transition = 'all 0.6s ease-out';
        observer.observe(card);
    });

    /* ── Scroll-reveal: project cards ── */
    const projectObserver = new IntersectionObserver((entries) => {
        entries.forEach((entry, i) => {
            if (entry.isIntersecting) {
                setTimeout(() => {
                    entry.target.style.opacity = '1';
                    entry.target.style.transform = 'translateY(0)';
                }, i * 120);
            }
        });
    }, { threshold: 0.1 });

    document.querySelectorAll('.project-card').forEach(card => {
        card.style.opacity = '0';
        card.style.transform = 'translateY(30px)';
        card.style.transition = 'all 0.7s ease-out';
        projectObserver.observe(card);
    });

    /* ── Scroll-reveal: team cards ── */
    const teamObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.style.opacity = '1';
                entry.target.style.transform = 'translateY(0)';
            }
        });
    }, { threshold: 0.2 });

    document.querySelectorAll('.team-card').forEach(card => {
        card.style.opacity = '0';
        card.style.transform = 'translateY(30px)';
        card.style.transition = 'all 0.8s ease-out';
        teamObserver.observe(card);
    });
});
